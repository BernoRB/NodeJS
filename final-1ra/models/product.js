class Product {
    constructor(id, timestamp, title, desc, price, thumbnail, code, stock) {
        this.id = id,
        this.timestamp = timestamp,
        this.title = title,
        this.desc = desc,
        this.price = price,
        this.thumbnail = thumbnail,
        this.code = code,
        this.stock = stock
    }
}

module.exports = Product
